# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution:
    def flatten(self, root: TreeNode) -> None:
        """
        Do not return anything, modify root in-place instead.
        """
        if not root:
            return
        # 递归，后续遍历
        self.flatten(root.left)
        self.flatten(root.right)
        if root.left:
            right = root.right
            root.right = root.left
            root.left = None
            # 右子树接原左子树的最右叶子节点
            while root.right:
                root = root.right
            root.right = right
        
        # 迭代
        cur = root
        while cur:
            if cur.left:
                p = cur.left
                while p.right:
                    p = p.right
                p.right = cur.right
                cur.right = cur.left
                cur.left = None
            cur = cur.right